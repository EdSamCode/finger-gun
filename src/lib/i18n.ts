/** Interpola variables: tr(T.es, 'level_completed', {n:3}) → 'NIVEL 3 COMPLETADO' */
export function tr(t: Translation, key: keyof Translation, vars?: Record<string, string | number>): string {
  const raw = t[key]
  if (typeof raw !== 'string') return ''
  if (!vars) return raw
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, String(v)),
    raw
  )
}

export function detectLang(): Lang {
  if (typeof window === 'undefined') return 'es'
  const saved = localStorage.getItem('fg_lang') as Lang | null
  if (saved && T[saved]) return saved
  const browser = navigator.language.split('-')[0]
  const map: Record<string, Lang> = {
    es: 'es', en: 'en', zh: 'zh', ru: 'ru', ja: 'ja',
    pt: 'pt', fr: 'fr', de: 'de', tr: 'tr', ar: 'ar', pl: 'pl',
  }
  return map[browser] ?? 'es'
}
